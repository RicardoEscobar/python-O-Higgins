"""
List Patterns
Common patterns when working with lists.
"""

# Create a list with range
lista = list(range(5))

# Specify a range with start and stop
lista = list(range(1,5))

# Specify a range with step
lista = list(range(0, 10, 2))

# Specify a range with negative step
lista = list(range(10, 0, -1))
print(lista)
